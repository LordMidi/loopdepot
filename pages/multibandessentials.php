      <h3>Thinking multiband in a new way</h3>
      <p>
        The basis of multiband is to have the <strong>frequency spectrum</strong> of an audio signal split into separate, dedicated areas.
        This approach is mainly used inside of a compressor.
        Until now!
        Loop Depot transferred this idea into other effect devices as well and created something new, leveraging the <strong>the power of the Reason Combinator 2</strong>. (Reason 12 or Reason+ is required)
      </p>
      <p>
        <strong>This pack contains 10 Combinator 2 effect patches</strong> which cover the basic range of must-have effects in a multiband way.
        Pitch down only the high frequencies, apply a reverb only on the mids, distort the lower band, pan the mids to left - these are some examples of <strong>what is possible now with Multiband Essentials</strong>.
      </p>
      <p>
        All the devices follow the same flow.
        <strong>The signal is split into four bands</strong>: low, mid:low, mid:high and high.
        Adjust the parameters for each band individually or bring them into sync if required.
        But of course a <strong>multiband compressor</strong> is included too.
      </p>
      <p>
        Included Combinator 2 effect patches:
        <ul>
          <li>Multiband Chorus</li>
          <li>Multiband Compressor</li>
          <li>Multiband Delay</li>
          <li>Multiband Distortion</li>
          <li>Multiband Flanger</li>
